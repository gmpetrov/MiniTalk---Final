#include					"libft.h"

void						ft_putnbr_fd(int n, int fd)
{
	if (n < 0 && (n *= (-1)))
		(void) ft_putchar_fd('-', fd);
	if ((n / 10) != 0)
		(void) ft_putnbr(n / 10);
	return (ft_putchar_fd((n % 10) + '0', fd));
}
