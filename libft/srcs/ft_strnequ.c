#include					"libft.h"

int							ft_strnequ(char const *s1,
									   char const *s2,
									   unsigned long n)
{
	if (!s1 || !s2 || *s1 != *s2)
		return (0x00);
	while (--n && n > 0x00 && *s1++ && *s2++)
		{
			if (*s1 != *s2)
				return (0x00);
		}
	return (0x01);
}
