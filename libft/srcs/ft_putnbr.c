#include					"libft.h"

void						ft_putnbr(int n)
{
	if (n < 0 && (n *= (-1)))
		(void) ft_putchar('-');
	if ((n / 10) != 0)
		(void) ft_putnbr(n / 10);
	return (ft_putchar((n % 10) + '0'));
}
