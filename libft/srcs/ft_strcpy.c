#include					"libft.h"

char						*ft_strcpy(char *s1,
									   char const *s2)
{
	int						i;

	i = 0;
	while (s2[i] != 0x00)
		{
			s1[i] = ((char *) s2)[i];
			i++;
		}
	s1[i] = 0x00;
	return (s1);
}
